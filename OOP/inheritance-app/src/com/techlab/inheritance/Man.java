public class Man {

	public void read() {
		System.out.println("Man is reading");
	}

	public void play() {
		System.out.println("Man is playing");
	}

	public void walk() {
		System.out.println("Man is walking");
	}
	
	public Man(){
		System.out.println("Man Created");
	}
}
